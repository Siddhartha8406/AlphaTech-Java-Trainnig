/*
 * Write a program to check if two numbers are equal or not.
 */

class CheckEqual {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        if (a == b) {
            System.out.println("a and b are equal");
        } else {
            System.out.println("a and b are not equal");
        }
    }
}
